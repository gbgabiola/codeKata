/**
 * Return the Sum of Two Numbers
 *
 * Create a function that takes two numbers as arguments and return their sum.
 *
 * Examples:
 * addition(3, 2) ➞ 5
 * addition(-3, -6) ➞ -9
 * addition(7, 3) ➞ 10
 *
 * Note: Don't forget to return the result.
 */

